import math

# Задаем начальные условия для цикла
x_start = -5.0
x_end = 5.0
step = 0.5

# Выводим заголовок таблицы
print("-------------------------")
print("|     x     |      y     |")
print("-------------------------")

# Инициализируем начальное значение x
x = x_start

# Используем циклический оператор 'while' для итерации по отрезку
while x <= x_end:
    y = 0.0  # Инициализируем y

    # Используем условные операторы 'if/elif/else' для выбора формулы
    if x <= 0:
        # Формула для x <= 0: y = x^4 * 2^x
        y = (x ** 4) * (2 ** x)
    elif 0 < x <= math.pi / 2:
        # Формула для 0 < x <= pi/2: y = (sin(x))^cos(x)
        # math.pi / 2 ≈ 1.57
        y = math.pow(math.sin(x), math.cos(x))
    else:
        # Формула для x > pi/2: y = x^(ln(x))
        # math.log(x) вычисляет натуральный логарифм (ln)
        y = math.pow(x, math.log(x))

    # Выводим строку таблицы с форматированием
    print(f"| {x:^9.2f} | {y:^10.4f} |")

    # Увеличиваем x на заданный шаг
    x += step

# Выводим окончание таблицы
print("-------------------------")